    if (topic === "orthography" && RUSSIAN_ORTHOGRAPHY_MASKS[index]) {
      const mask = RUSSIAN_ORTHOGRAPHY_MASKS[index];
      return [mask.text, mask.options[0], ...mask.options.slice(1), row[5]];
    }

