    const optionTexts = isMultiple
      ? preparedRow.options
      : arrangeSingleAnswerOptions(preparedRow.options, correctTexts[0], index);
    const correct = isMultiple
      ? optionTexts.map((text, optionIndex) => (correctTexts.includes(text) ? LETTERS[optionIndex] : null)).filter(Boolean)
      : LETTERS[optionTexts.indexOf(correctTexts[0])];

