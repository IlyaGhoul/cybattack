  function rotate(items, shift) {
    const normalized = items.length === 0 ? 0 : shift % items.length;
    return [...items.slice(normalized), ...items.slice(0, normalized)];
  }

  function singleQuestion(university, topic, id, text, correctText, distractors, explanation) {
    const optionTexts = rotate([correctText, ...distractors].slice(0, 4), id.length + text.length);
    return {
      id,
      university,
      topic,
      type: "single",
      text,
      options: optionTexts.map((optionText, index) => ({ letter: LETTERS[index], text: optionText })),
      correct: LETTERS[optionTexts.indexOf(correctText)],
      explanation,
    };
  }

  function multipleQuestion(university, topic, id, text, correctTexts, distractors, explanation) {
    const optionTexts = rotate([...correctTexts, ...distractors].slice(0, 6), id.length + text.length);
    return {
      id,
      university,
      topic,
      type: "multiple",
      text,
      hint: "Выберите несколько ответов.",
      options: optionTexts.map((optionText, index) => ({ letter: LETTERS[index], text: optionText })),
      correct: optionTexts
        .map((optionText, index) => (correctTexts.includes(optionText) ? LETTERS[index] : null))
        .filter(Boolean),
      explanation,
    };
  }

  function matchingQuestion(university, topic, id, text, pairs, distractors, explanation) {
    const options = [...pairs.map((pair) => pair.answer), ...distractors].map((answer, index) => ({
      letter: LETTERS[index],
      text: answer,
    }));
    const correct = Object.fromEntries(
      pairs.map((pair, index) => [
        String(index + 1),
        options.find((option) => option.text === pair.answer).letter,
      ]),
    );

    return {
      id,
      university,
      topic,
      type: "matching",
      text,
      prompts: pairs.map((pair, index) => ({ id: String(index + 1), text: pair.prompt })),
      options,
      correct,
      explanation,
    };
  }

